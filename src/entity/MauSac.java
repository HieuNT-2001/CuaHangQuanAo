package entity;

public class MauSac {

    private int maMS;
    private String mauSac;

    public MauSac() {
    }

    public MauSac(int maMS, String mauSac) {
        this.maMS = maMS;
        this.mauSac = mauSac;
    }

    public int getMaMS() {
        return maMS;
    }

    public void setMaMS(int maMS) {
        this.maMS = maMS;
    }

    public String getMauSac() {
        return mauSac;
    }

    public void setMauSac(String mauSac) {
        this.mauSac = mauSac;
    }

}
